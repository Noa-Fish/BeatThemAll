public class Carte {
    public String lieu;
    public String nom;
    public int debut;
    public int fin;
    public int etape;

    public Carte(String lieu, String nom, int debut, int fin, int etape) {
        this.lieu = lieu;
        this.nom = nom;
        this.debut = debut;
        this.fin = fin;
        this.etape = etape;
    }
}
